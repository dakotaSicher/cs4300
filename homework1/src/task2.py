def dataType(arg1):
    return type(arg1)

def add(op1, op2):
    return op1 + op2

def sub(op1, op2):
    return op1-op2

def mult(op1, op2):
    return op1*op2

def div(op1, op2):
    return op1/op2

def mod(op1, op2):
    return op1%op2

def And(op1, op2):
    return op1 and op2

def Or(op1, op2):
    return op1 or op2